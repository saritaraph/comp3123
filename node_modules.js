function right(str){
    if(str.length < 3){
        return str
    }
    let lastThree = str.slice(str.length-3)
    let other = str.slice(0, str.length-3)
    return lastThree + other
}
console.log(right("Python"))
console.log(right("JavaScript"))
console.log(right("Hi"))


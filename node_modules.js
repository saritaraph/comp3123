function capWords(str){
    let words = str.split(" ")
    let result = []
    for(let i=0; i < words.length; i++){
        let word = words[i]
        let firstWord = word.charAt(0).toUpperCase()
        let otherWord = word.slice(1)
        result.push(firstWord + otherWord)
    }
    return result.join(" ")
}
console.log(capWords("the quick brown fox"))

